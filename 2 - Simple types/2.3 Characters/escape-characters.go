// \' for a single quote
var singleQuote = '\''

// " (for rune) or \" (for string) for a double quote"
var doubleQuote = '"'

// \\ for a backslash
var backslash = '\\'

// \a for an alert character
var alert = '\a'

// \n for a newline
var newline = '\n'

// \r for a carriage return
var carriageReturn = '\r'

// \t for a tab
var tab = '\t'

// \v for a vertical tab
var verticalTab = '\v'

// \x followed by hexadecimal digits for a Unicode character
var unicode = '\x41' // 'A'
