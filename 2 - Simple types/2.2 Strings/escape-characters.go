// \n Insert a newline.
// \b Insert a backspace.
// \r Insert a carriage return.
// \t Insert a tab.
// \' Insert a single quote.
// \" Insert a double quote.
// \\ Insert a backslash.

// Here is an example of using escape characters:
var str = "This is a string with a single quote: \' and a double quote: \" and a backslash: \\"
var str2 = "This is a string with a newline\nand a tab\tand a backspace\b"
fmt.Println(str)
fmt.Println(str2)
// Output: This is a string with a single quote: ' and a double quote: " and a backslash: \
// This is a string with a newline
// and a tab    and a backspace
